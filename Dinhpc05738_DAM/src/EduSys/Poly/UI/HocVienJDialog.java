    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int option = JOptionPane.showOptionDialog(this, "Bạn chắc muốn thoát chương trình", "Xác nhận",
            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null,
            new Object[]{"Yes", "No"}, "No");

        if (option == JOptionPane.YES_OPTION) {
            Thread loadingThread = new Thread(new Runnable() {
                public void run() {
                    try {
                        // Giả lập quá trình tải dữ liệu trong 2 giây
                        Thread.sleep(1000);
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }

                    // Tạo và hiển thị form đăng nhập sau khi quá trình tải dữ liệu hoàn thành
                    SwingUtilities.invokeLater(new Runnable() {
                        public void run() {
                            EduSysJFrame p = new EduSysJFrame();
                            p.setVisible(true);
                        }
                    });

                    // Đóng form hiện tại
                    dispose();
                }
            });

            loadingThread.start(); // Bắt đầu tiến trình tải dữ liệu
        }
    }//GEN-LAST:event_jButton1ActionPerformed

