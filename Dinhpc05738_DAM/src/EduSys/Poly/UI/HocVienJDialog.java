    private javax.swing.JTable tblDanhSachHocVien;
    private javax.swing.JTable tblDanhSachNguoiHoc;
    private javax.swing.JTextField txtTimKiem;
    // End of variables declaration//GEN-END:variables

    private void init() {
        setIconImage(XImage.getAppicon());
        this.setLocationRelativeTo(null);
        fillComboxChuyenDe();
    }

    void fillComboxChuyenDe() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboChuyenDe.getModel();
        model.removeAllElements();
        try {
            List<ChuyenDe> list = cdDao.selectAll();
            for (ChuyenDe cd : list) {
                model.addElement(cd);
            }
            fillComboBoxKhoaHoc();
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn,fillComboboxCHuyenDe");
        }
    }

    void fillComboBoxKhoaHoc() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboKhoaHoc.getModel();
        model.removeAllElements();
        try {
            ChuyenDe chuyenDe = (ChuyenDe) cboChuyenDe.getSelectedItem();
            if (chuyenDe != null) {
                List<KhoaHoc> list = khDao.selectByChuyenDe(chuyenDe.getMaCD());
                for (KhoaHoc kh : list) {
                    model.addElement(kh);
                }
            }
            fillTableHocVien();
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu, FillComboxKhoaHoc");
        }
    }

    void fillTableNguoiHoc() {
        DefaultTableModel tblModel = (DefaultTableModel) tblDanhSachNguoiHoc.getModel();
        tblModel.setRowCount(0);
        try {
            KhoaHoc kh = (KhoaHoc) cboKhoaHoc.getSelectedItem();
            if (kh != null) {
                System.out.println("MaKH: " + kh.getMaKH());
                String keyWord = txtTimKiem.getText();
                List<NguoiHoc> list = ngHocDao.selectNotInCourse(kh.getMaKH(), keyWord);
                for (NguoiHoc nh : list) {
                    Object[] row = {nh.getMaNH(), 
                        nh.getHoTen(),
                        nh.isGioiTinh() ? "Nam" : "Nữ", 
                        nh.getNgaySinh(),
                        nh.getDienThoai(), 
                        nh.getEmail()
                    };
                    tblModel.addRow(row);
                }
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu, fillTableNguoiHoc");
        }
    }

    void fillTableHocVien() {
        DefaultTableModel tblModel = (DefaultTableModel) tblDanhSachHocVien.getModel();
        tblModel.setRowCount(0);
        try {
            KhoaHoc kh = (KhoaHoc) cboKhoaHoc.getSelectedItem();
            if (kh != null) {
                System.out.println("FillTableHocVien MaKH: " + kh.getMaKH());
                List<HocVien> list = hvDao.selectByKhoaHoc(kh.getMaKH());
                System.out.println("list: " + list.size());
                for (int i = 0; i < list.size(); i++) {
                    HocVien hv = list.get(i);
                    String hoTen = ngHocDao.selectById(hv.getMaNH()).getHoTen();
                    Object[] row = {i + 1, hv.getMaHV(), hv.getMaNH(), hoTen, hv.getDiem()};
                    tblModel.addRow(row);
                }
            }
            fillTableNguoiHoc();
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu, fillTableHocVien");
        }
    }

    void addHocVien() {
        KhoaHoc kh = (KhoaHoc) cboKhoaHoc.getSelectedItem();
        for (int row : tblDanhSachNguoiHoc.getSelectedRows()) {
            HocVien hv = new HocVien();
            hv.setMaKH(kh.getMaKH());
            hv.setDiem(0);
            hv.setMaNH((String) tblDanhSachNguoiHoc.getValueAt(row, 0));
            hvDao.insert(hv);
        }
        fillTableHocVien();
        jTabbedPane1.setSelectedIndex(0);
    }

    void removeHocVien() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Bạn không đủ quyền để xóa học viên");
        } else {
            if (MsgBox.confirm(this, "Bạn muốn xóa các học viên được chọn")) {
                for (int row : tblDanhSachHocVien.getSelectedRows()) {
                    int mahv = (int) tblDanhSachHocVien.getValueAt(row, 1);
                    hvDao.delete(String.valueOf(mahv));
                }
            }
        }
        this.fillTableHocVien();
    }

    void updateHocVien() {
        for (int i = 0; i < tblDanhSachHocVien.getRowCount(); i++) {
            int mahv = (int) tblDanhSachHocVien.getValueAt(i, 1);
            double diem = (Double.parseDouble(tblDanhSachHocVien.getValueAt(i, 4).toString()));
            HocVien hocVien = hvDao.selectById(String.valueOf(mahv));
            hocVien.setDiem(diem);
            hvDao.update(hocVien);
        }
        MsgBox.alert(this, "Cập nhật điểm thành công");
    }
